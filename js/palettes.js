// Build lookup map including aliases
function getPaletteByKey(key) {
  return PALETTES[key] || null;
}

function getAllPaletteOptions() {
  const options = [];
  for (const [key, palette] of Object.entries(PALETTES)) {
    options.push({ key, name: palette.name, description: palette.description });
  }
  return options;
}

// Search by name or alias (case-insensitive)
function findPalette(query) {
  const q = query.toLowerCase();
  for (const [key, palette] of Object.entries(PALETTES)) {
    if (palette.name.toLowerCase() === q) return { key, palette };
    for (const alias of palette.aliases) {
      if (alias.toLowerCase() === q) return { key, palette };
    }
  }
  return null;
}
